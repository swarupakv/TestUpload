package com.programs;

public class LargestOfTwoNumbers {

	public static void main(String[] args) {
		
		int a = 40;
		int b = 23;
		
		if(a>b)
		{
			System.out.println("A is larger");
		}
		else if (b>a)
		{
			System.out.println("B is larger");
		}
		else
		{
			System.out.println("Both are equal");
		}
	}

}


//A is larger