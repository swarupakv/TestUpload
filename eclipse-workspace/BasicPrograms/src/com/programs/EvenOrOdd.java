public class EvenOrOdd {

	public static void main(String[] args) {
		
		// 45/2 = Que
		//45%2 = Rem   If Rem = 0, then number is even else odd
		
		int n = 133;
		if(n%2==0)
		{
			System.out.println("number is even");
		}
		else 
		{
			System.out.println("number is odd");
		}
	}
}

